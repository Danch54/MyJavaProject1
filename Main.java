public class Main {

    public static void main(String[] args) {
        // Объявление переменных
        int age = 32;                   // Целое число
        double salary = 100000.00;      // Число с плавающей точкой
        String name = "Daniil";         // Текстовая строка
        boolean isEmployed = true;      // Логическое значение

        // Вывод переменных на консоль
        System.out.println("Имя: " + name);          // Вывод имени
        System.out.println("Возраст: " + age);        // Вывод возраста
        System.out.println("Зарплата: " + salary);    // Вывод зарплаты
        System.out.println("Трудоустроен: " + isEmployed); // Вывод статуса трудоустройства
    } 
}


    

    